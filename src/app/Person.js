export class Person {
    constructor(firstName, lastName, address1, address2, city, state, zip, cardToken) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.address1 = address1;
        this.address2 = address2;
        this.city = city;
        this.state = state;
        this.zip = zip;
        this.cardToken = cardToken;
    }
};
